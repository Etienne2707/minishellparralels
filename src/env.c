#include "minishell.h"

int	ft_env(char *envp[])
{
	int	i;

	i = 0;
	while (envp[i])
	{
		if (printf("%s\n", envp[i++]) == 0)
			return (EXIT_FAILURE);
	}
	return (EXIT_SUCCESS);
}
