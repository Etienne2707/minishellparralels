#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*c1;
	size_t			i;

	i = -1;
	c1 = (unsigned char *)s;
	while (++i < n)
		c1[i] = c;
	return (s);
}
